#!/usr/bin/env dart

import 'dart:io';
import 'package:args/command_runner.dart';
import 'package:appforge_cli/commands/create_command.dart';
import 'package:mason_logger/mason_logger.dart';

void main(List<String> arguments) async {
  final logger = Logger();

  final runner = CommandRunner<int>(
    'appforge',
    'Generate production-ready Flutter apps instantly',
  )
    ..addCommand(CreateCommand(logger: logger))
    ..argParser.addFlag(
      'version',
      abbr: 'v',
      negatable: false,
      help: 'Print the current version.',
    )
    ..argParser.addFlag(
      'commands',
      negatable: false,
      help: 'List all available commands.',
    );

  try {
    final results = runner.parse(arguments);

    // Show version
    if (results['version'] == true) {
      _printVersion(logger);
      exit(0);
    }

    // Show all commands
    if (results['commands'] == true) {
      _printAllCommands(logger, runner);
      exit(0);
    }

    // Show welcome screen if no arguments
    if (arguments.isEmpty) {
      _printWelcome(logger);
      exit(0);
    }

    final exitCode = await runner.runCommand(results) ?? 0;
    exit(exitCode);
  } on UsageException catch (e) {
    logger
      ..err(e.message)
      ..info('')
      ..info(e.usage);
    exit(64);
  } catch (e) {
    logger.err('$e');
    exit(1);
  }
}

void _printVersion(Logger logger) {
  logger.info('appforge_cli version: 0.1.2');
}

